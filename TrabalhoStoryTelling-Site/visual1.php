    <div class="content-section">
      <p class="content-text">
        A **Teoria das Cores** é um campo que explora como as cores interagem e como elas podem ser combinadas para criar efeitos visuais harmoniosos ou impactantes. Desde a antiguidade, filósofos e cientistas como Aristóteles e Isaac Newton estudaram o comportamento das cores e sua relação com a luz.
      </p>
      
      <img src="img/teoria-cores.jpg" alt="Descrição da Imagem 1" class="content-image">
      
      <p class="content-text">
        Uma das bases da teoria é o **Círculo Cromático**, que organiza as cores em um espectro visível. Ele é composto por cores primárias (vermelho, azul e amarelo), cores secundárias (laranja, verde e roxo) e cores terciárias, que surgem das misturas entre elas. O círculo é fundamental para entender combinações como cores complementares, análogas e triádicas.
      </p>
      
      <img src="img/circulo-cromatico.png" alt="Descrição da Imagem 2" class="content-image">
      
      <p class="content-text">
        A aplicação da teoria das cores é crucial em áreas como design gráfico, moda, decoração e artes visuais. Cores transmitem emoções e sensações: enquanto o azul sugere calma e serenidade, o vermelho evoca energia e paixão. A escolha cuidadosa das cores pode impactar diretamente a percepção e o comportamento das pessoas.
      </p>
      
      <img src="img/psicologia-cores.jpg" alt="Descrição da Imagem 3" class="content-image" width="400">
      
      <p class="content-text">
        Outro conceito importante é o uso de cores em diferentes sistemas: cores **aditivas** (como no RGB, utilizado para telas digitais) e **subtrativas** (como no CMYK, usado em impressão). Cada sistema aborda a mistura de cores de maneira distinta, adaptando-se ao meio em que será aplicado.
      </p>

      <p class="content-text">
        Estudar a Teoria das Cores permite explorar combinações criativas e transmitir mensagens de forma mais eficaz. Seja no design ou na arte, dominar as cores é essencial para criar composições visualmente atrativas e impactantes.
      </p>

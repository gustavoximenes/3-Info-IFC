    <div class="content-section">
      <p class="content-text">
        A Jornada do Herói, ou Monomito, é uma estrutura narrativa identificada por Joseph Campbell que descreve o ciclo de aventuras pelo qual um protagonista passa. Esse modelo universal é dividido em três atos principais: **Partida**, **Iniciação** e **Retorno**.
      </p>
      
      <img src="img/Jornada-Heroi.jpg" alt="Descrição da Imagem 1" class="content-image">
      
      <p class="content-text">
        Christopher Vogler, em seu livro *"A Jornada do Escritor"*, expandiu o conceito da Jornada do Herói, adaptando-o para a criação de roteiros e narrativas modernas. Ele detalhou como esses elementos podem ser usados para construir histórias envolventes e personagens memoráveis, com base na estrutura clássica de Campbell.
      </p>
      
      <img src="img/Heroi.jpg" alt="Descrição da Imagem 2" class="content-image">
      
      <p class="content-text">
        Na **Partida**, o herói é chamado para sair de sua zona de conforto. Ele enfrenta um momento de resistência inicial, mas acaba aceitando o chamado, muitas vezes com a ajuda de um mentor. Segundo Vogler, essa etapa prepara o público para a transformação que está por vir, criando uma conexão emocional com o protagonista.
      </p>
      
      <img src="img/Jornada-Escritor.jpg" alt="Descrição da Imagem 3" class="content-image">
      
      <p class="content-text">
        Durante a **Iniciação**, o herói enfrenta desafios e adversidades, que o transformam e o fortalecem. Vogler destaca que essa fase é crucial para construir o clímax da narrativa, onde as provas e desafios representam obstáculos internos e externos que o herói deve superar.
      </p>
      
      <p class="content-text">
        Finalmente, no **Retorno**, o herói volta ao mundo comum, trazendo consigo o aprendizado ou o prêmio conquistado. Vogler reforça que o retorno deve simbolizar não apenas a resolução da história, mas também o impacto da transformação do herói em sua comunidade.
      </p>

      <p class="content-text">
        O livro *"A Jornada do Escritor"* continua sendo uma referência essencial para escritores, roteiristas e criadores de histórias, oferecendo insights práticos sobre como aplicar a estrutura da Jornada do Herói em diferentes contextos narrativos.
      </p>

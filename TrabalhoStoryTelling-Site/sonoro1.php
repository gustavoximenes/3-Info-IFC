    <div class="content-title">A Imersão do Som em Filmes</div>
    
    <div class="content-section">
      <p class="content-text">
        O som desempenha um papel crucial na criação de experiências imersivas em filmes e outros meios audiovisuais. Mais do que um complemento visual, ele é responsável por evocar emoções, criar tensão e guiar a narrativa de maneira única e envolvente.
      </p>
      
      <img src="img/imersao-sonora.png" alt="Descrição da Imagem 1" class="content-image">
      
      <p class="content-text">
        Uma técnica fundamental é o **uso da trilha sonora**, que define o tom e intensifica a emoção de cada cena. Desde as composições épicas de John Williams em *Star Wars* até as trilhas minimalistas de Hans Zimmer, a música transporta o público para diferentes mundos e estados de espírito.
      </p>
      
      <p class="content-text">
        Além da música, os **efeitos sonoros** (foley) são essenciais para construir realismo. O som de passos, o ranger de uma porta ou o impacto de uma explosão são cuidadosamente criados para aumentar a verossimilhança e manter o público engajado. Esses elementos muitas vezes passam despercebidos, mas sua ausência é rapidamente notada.
      </p>
      
      <img src="img/imersao-sonora-livro.jpg" alt="Descrição da Imagem 3" class="content-image">
      
      <p class="content-text">
        Outra ferramenta poderosa é o **design de som**, que manipula sons de forma criativa para construir atmosferas. Em filmes de terror, por exemplo, sons abafados e frequências baixas criam um senso de desconforto e antecipação. Já em filmes de ficção científica, sons futuristas e dinâmicos ajudam a imaginar novas tecnologias e realidades.
      </p>

      <p class="content-text">
        A integração do som com sistemas de áudio como Dolby Atmos trouxe uma nova dimensão de imersão. Esses sistemas posicionam o som em um espaço tridimensional, permitindo que o público sinta a profundidade e o movimento dentro da cena.
      </p>

      <p class="content-text">
        Em suma, o som não apenas complementa a imagem, mas é um elemento narrativo por si só. Ele transforma uma cena simples em uma experiência memorável, capturando a atenção do público e levando-o a um nível mais profundo de envolvimento emocional e sensorial.
      </p>
